DROP TABLE recipes

CREATE TABLE recipes (
    id INT NOT NULL PRIMARY KEY AUTO_INCREMENT, createdAt DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT 'Time Created', updatedAt DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Last Update', title VARCHAR(255) NOT NULL, instructions VARCHAR(1000) NOT NULL, img VARCHAR(500) NOT NULL, category ENUM(
        "cheese", "italian", "soup", "mexican", "coffee"
    ), creatorId VARCHAR(255) NOT NULL
)